                        <h4 class="post-headline">Sobre a Plataforma</h4>
                    </a>
                    <p align="justify">A plataforma Bordadeiras do Brasil, faz parte do projeto A Arte de Bordar
                        pelo
                        Brasil, criado em 2019, pela artista Fernanda Nadal, designer de bordados multifacetada, que
                        atua como professora, empresária, mentora e escritora. <br>
                        <br>
                        O projeto, realizado através da Lei Federal de Incentivo à Cultura com o patrocínio de
                        empresas
                        incentivadoras, já contemplou mais de 400 mulheres com formação na arte do bordado em
                        pedraria.
                        Em 2024, criou-se esta plataforma, como forma de propagar o trabalho das participantes dos
                        cursos realizados por Fernanda, junto à pessoas e empresas interessadas na mão de obra com o
                        bordado em pedraria.<br>
                        <br>
                        A plataforma Bordadeiras do Brasil representa um pilar fundamental nessa missão de
                        capacitação,
                        visando oferecer oportunidades para que indivíduos possam obter uma fonte de renda por meio
                        do
                        bordado. Por meio dessa plataforma, interessados em contratar serviços de bordado com
                        pedrarias
                        podem facilmente encontrar profissionais qualificados em suas regiões de interesse, seja
                        individualmente ou como entidades corporativas.<br>
                        <br>
                        <strong>QUEM PODE PARTICIPAR:</strong><br>
                        O projeto é destinado a mulheres participantes do curso A Arte de Bordar com a Fernanda. As
                        inscrições são abertas conforme patrocínios realizados nas cidades, através de empresas que
                        destinam parte do seu Imposto de Renda à cultura. <br>
                        <br>
                        As participantes do curso interessadas em ter o seu material na plataforma, receberão um
                        tecido
                        com o traço pré-determinado e entregue pela Fernanda durante a formação, que deverá ser
                        bordado
                        com as pedrarias disponíveis, utilizando-se dos pontos ensinados em aula. A própria Fernanda
                        analisará o material recebido por cada aluna, observando a habilidade com os pontos, o
                        cuidado
                        com o material trabalhado e a responsabilidade com os prazos propostos durante a formação.
                        <br>
                        <br>
                        As alunas que atingirem ao esperado, poderão inserir o bordado realizado, além de seu
                        contato
                        para que interessados possam entrar em contato diretamente com as mulheres.
                    </p>

