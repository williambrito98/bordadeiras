                    <a href="#">
                        <h4 class="post-headline">Sobre a Plataforma</h4>
                    </a>
                    <p align="justify">
                        A Plataforma Bordadeiras do Brasil faz parte do projeto A Arte de Bordar pelo Brasil, que foi
                        criado em 2019 pela artista Fernanda Nadal, designer de bordados multifacetada, que atua como
                        professora, empresária, mentora e escritora.
                        <br>
                        <br>
                        O projeto, hoje realizado através da Lei Federal de Incentivo à Cultura com o patrocínio de
                        empresas incentivadoras, já contemplou mais de 400 mulheres com formação na arte do bordado em
                        pedrarias, e já passou pelas cidades de Montenegro/RS, João Monlevade/MG, Campinas/SP,
                        Indaiatuba/SP e Mogi Guaçu/SP.
                        <br>
                        <br>
                        EEm 2023, deu-se início à criação desta Plataforma, Bordadeiras do Brasil, com a proposta de
                        possibilitar a divulgação do trabalho das participantes dos cursos realizados no projeto, que
                        tenham a intenção de dar continuidade a trabalhos na área, junto à pessoas e empresas
                        interessadas na mão de obra com o bordado em pedrarias.
                        <br>
                        <br>
                        Esta plataforma pretende oferecer oportunidades para que mulheres possam obter uma fonte de
                        renda por meio do bordado em pedrarias. Através dela, pretende-se que interessados em contratar
                        serviços de bordado em pedrarias, seja individualmente ou como entidades corporativas, possam
                        encontrar profissionais qualificadas em suas regiões de interesse.
                    </p>
                    <br>

                    <p align="justify">
                        <strong>QUEM PODE PARTICIPAR:</strong>
                        <br>
                        Esta plataforma é destinada a mulheres participantes do curso A Arte de Bordar pelo Brasil,
                        realizado através da Lei Federal de Incentivo à Cultura. As inscrições são abertas conforme
                        patrocínios realizados nas cidades, através de empresas que destinam parte do seu Imposto de
                        Renda à cultura, via Lei de Incentivo.
                        <br>
                        <br>
                        As participantes de cada edição do curso interessadas em dar continuidade no aprendizado
                        adquirido durante a formação, poderão ter o seu material nesta plataforma, de acordo com uma
                        análise feita pela própria Fernanda junto à sua equipe, no intuito de divulgar os seus
                        trabalhos, para que interessados possam entrar em contato diretamente com as alunas do projeto.
                        <br>
                        <br>
                        A própria Fernanda analisará o material recebido por cada aluna,
                        observando a habilidade com os pontos, o cuidado com o material trabalhado e a responsabilidade
                        com os prazos propostos durante a formação. As alunas que atingirem ao esperado e que
                        pretenderem seguir se especializando, poderão ter o bordado realizado na formação aqui na
                        plataforma, junto do seu contato, para que interessados possam entrar em contato diretamente com
                        elas.
                    </p>

